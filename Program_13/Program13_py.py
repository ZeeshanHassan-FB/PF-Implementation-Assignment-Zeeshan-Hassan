# Write a program using define directive to find the area of a circle. The formula to find the area of a circle is:
#     Area =πR^2      The value of π is 3.1417
def program13():
    # Input
    PI = 3.1417
    radius = 3

    # Processing
    area = PI * (radius * radius)

    # Output
    print(f"Area of circle: {area:.2f}")
